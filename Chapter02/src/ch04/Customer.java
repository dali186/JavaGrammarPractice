package ch04;

public class Customer {

	public String customerName;
	public int customerHeight;
	public int customerWeight;
	public String customerPhonenumber;
	public String customerPlace;
	
	public Customer(String ctName, int ctHeight, int ctWeight, String ctPhonenumber, String ctPlace) {
		this.customerName = ctName;
		this.customerHeight = ctHeight;
		this.customerWeight = ctWeight;
		this.customerPhonenumber = ctPhonenumber;
		this.customerPlace = ctPlace;
	}
}
